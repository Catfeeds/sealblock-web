package com.woodare.sealblock.rpc;

import com.fasterxml.jackson.annotation.JsonFormat;

/**
 * @author Luke
 */
@JsonFormat(shape = JsonFormat.Shape.OBJECT)
public class ZeroRPCHeader {
	private int v = 3;
	private String message_id;
	private String response_to;

	/**
	 * @return the v
	 */
	public int getV() {
		return v;
	}

	/**
	 * @param v
	 *            the v to set
	 */
	public void setV(int v) {
		this.v = v;
	}

	/**
	 * @return the message_id
	 */
	public String getMessage_id() {
		return message_id;
	}

	/**
	 * @param message_id
	 *            the message_id to set
	 */
	public void setMessage_id(String message_id) {
		this.message_id = message_id;
	}

	/**
	 * @return the response_to
	 */
	public String getResponse_to() {
		return response_to;
	}

	/**
	 * @param response_to
	 *            the response_to to set
	 */
	public void setResponse_to(String response_to) {
		this.response_to = response_to;
	}

}
